    <div class="grid_8 description">
        <p>
            A class group project to redesign an existing site using user-centered principles.  The site chosen by our group was the Washington Yacht Club due
            to my personal involvement in the club and the messy state of their <a href="https://sites.google.com/a/washingtonyachtclub.org/home/" target="_blank">previous site</a>.
            The project was completed over the course of 9 weeks with the cooperation of the club officers as stakeholders.
        </p>
        <br>
        <p><b>Goals: </b>Improve Usability and Visual Design to better present the character of the club and to allow for easier access to club information and member resources</p>
        <p><b>Personal Role: </b>Project Manager, Club Contact Person, Visual Designer, Programmer</p>
        <p><b>Technologies Used: </b>SimpleSort, Axure, Photoshop, HTML & CSS, Google Sites</p>
        <p><b>Challenges: </b>Working within the context of Google Sites to reduce the technical burden of continuing use. Also intelligently reducing the amount of published content
            while maintaining a consistent experience throughout the site</p>
    </div>

