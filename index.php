                <h2>Why User Experience?</h2>
                <p>
                    As much as I loved the problem solving and detail-oriented aspects of QA, I began to realize that my favorite
                    part of the job was working with interfaces and collaborating with designers and engineers in order to improve them.
                    This led me naturally toward UX and the ways that I could utilize my eye for quality and talent for identifying patterns
                    to design features and interfaces that delight the user.
                </p>
                <h2>What drives your design?</h2>
                <p>
                    Empathy and a user-centered focus is very important for me as I believe that only by first understanding
                    the users and their motivations and work flows can you design software that enables them to be successful.
                    I believe that good design exists in the balance between flexibility and simplicity and functions best when
                    it is both useful and usable. I think that good interfaces should be invisible, that they should keep the
                    user's attention on their task, not the process of completing the task.
                </p>
